/*==============================================================================
SECTION 4 : PRODUCT PERFORMANCE ANALYSIS

Objective:
Evaluate product performance by identifying high-performing
products, product categories and comparing revenue
against business benchmarks.
==============================================================================*/


/*
Business Question 21:
Which products generated more than ₹10,000 in revenue?

Purpose:
Identify products that contribute significantly
to the overall revenue.
*/

SELECT
    p.product_detail,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_detail
HAVING
    Revenue > 10000
ORDER BY
    Revenue DESC;



/*
Business Question 22:
Which product categories generated more than ₹50,000 in revenue?

Purpose:
Identify high-performing product categories based on revenue.
*/

SELECT
    p.product_category,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_category
HAVING
    Revenue > 50000
ORDER BY
    Revenue DESC;



/*
Business Question 23:
Which products were sold in more than 500 transactions?

Purpose:
Identify products with consistently high customer demand.
*/

SELECT
    p.product_detail,
    COUNT(t.transaction_id) AS Total_Transactions
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_detail
HAVING
    Total_Transactions > 500
ORDER BY
    Total_Transactions DESC;



/*
Business Question 24:
Which product categories sold more than 10,000 units?

Purpose:
Identify product categories with the highest sales volume.
*/

SELECT
    p.product_category,
    SUM(t.transaction_qty) AS Quantity_Sold
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_category
HAVING
    Quantity_Sold > 10000
ORDER BY
    Quantity_Sold DESC;



/*
Business Question 25:
Which products generated above-average revenue?

Purpose:
Compare each product's revenue against the
overall average product revenue.
*/

SELECT
    p.product_detail,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_detail
HAVING
    Revenue >
(
    SELECT AVG(Product_Revenue)
    FROM
    (
        SELECT
            SUM(t.transaction_qty * p.unit_price) AS Product_Revenue
        FROM transactions t
        JOIN products p
            ON t.product_id = p.product_id
        GROUP BY
            p.product_detail
    ) AS AvgRevenue
)
ORDER BY
    Revenue DESC;





/*==============================================================================
SECTION 5 : BUSINESS INSIGHTS

Objective:
Generate actionable business insights by classifying
products, stores and customer orders using SQL.
==============================================================================*/


/*
Business Question 26:
Classify products based on total revenue.

Purpose:
Categorize products into High, Medium and Low performers
to support pricing and inventory decisions.
*/

SELECT
    p.product_detail,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue,
    CASE
        WHEN SUM(t.transaction_qty * p.unit_price) >= 50000
            THEN 'High Performer'
        WHEN SUM(t.transaction_qty * p.unit_price) >= 20000
            THEN 'Medium Performer'
        ELSE 'Low Performer'
    END AS Performance
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_detail
ORDER BY
    Revenue DESC;



/*
Business Question 27:
Classify stores based on revenue.

Purpose:
Evaluate overall store performance based on
total revenue generated.
*/

SELECT
    s.store_location,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue,
    CASE
        WHEN SUM(t.transaction_qty * p.unit_price) >= 150000
            THEN 'Excellent'
        WHEN SUM(t.transaction_qty * p.unit_price) >= 100000
            THEN 'Good'
        ELSE 'Needs Improvement'
    END AS Store_Performance
FROM transactions t
JOIN stores s
    ON t.store_id = s.store_id
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    s.store_location
ORDER BY
    Revenue DESC;



/*
