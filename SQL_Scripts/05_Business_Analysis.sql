/*==============================================================================
SECTION 1 : REVENUE ANALYSIS

Objective:
Analyze revenue generated by the coffee shop and identify the
highest revenue-generating products, categories and store locations.
==============================================================================*/


/*
Business Question 1:
What is the total revenue generated?

Purpose:
Calculate the total sales revenue generated by the coffee shop.
*/

SELECT
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Total_Revenue
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id;



/*
Business Question 2:
What is the average order value?

Purpose:
Calculate the average value of each customer transaction.
*/

SELECT
    ROUND(AVG(t.transaction_qty * p.unit_price), 2) AS Average_Order_Value
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id;



/*
Business Question 3:
Which product generated the highest revenue?

Purpose:
Identify the product contributing the highest revenue.
*/

SELECT
    p.product_detail,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_detail
ORDER BY
    Revenue DESC;



/*
Business Question 4:
How much revenue was generated by each product category?

Purpose:
Compare revenue generated across product categories.
*/

SELECT
    p.product_category,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue
FROM transactions t
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    p.product_category
ORDER BY
    Revenue DESC;



/*
Business Question 5:
How much revenue was generated by each store location?

Purpose:
Compare revenue generated by each store.
*/

SELECT
    s.store_location,
    ROUND(SUM(t.transaction_qty * p.unit_price), 2) AS Revenue
FROM transactions t
JOIN stores s
    ON t.store_id = s.store_id
JOIN products p
    ON t.product_id = p.product_id
GROUP BY
    s.store_location
ORDER BY
    Revenue DESC;





